public class S_Guatemala {
    String NombreG, PrecioG, DescripcionG;

    public S_Guatemala(String NombreG, String PrecioG, String DescripcionG) {
        this.NombreG = NombreG;
        this.PrecioG = PrecioG;
        this.DescripcionG = DescripcionG;
    }

    public String getNombreG() {
        return NombreG;
    }

    public void setNombreG(String NombreG) {
        this.NombreG = NombreG;
    }

    public String getPrecioG() {
        return PrecioG;
    }

    public void setPrecioG(String PrecioG) {
        this.PrecioG = PrecioG;
    }

    public String getDescripcionG() {
        return DescripcionG;
    }

    public void setDescripcionG(String DescripcionG) {
        this.DescripcionG = DescripcionG;
    }
    
}

public class S_Jalapa {
    String NombreJ, PrecioJ, DescripcionJ;

    public S_Jalapa(String NombreJ, String PrecioJ, String DescripcionJ) {
        this.NombreJ = NombreJ;
        this.PrecioJ = PrecioJ;
        this.DescripcionJ = DescripcionJ;
    }

    public String getNombreJ() {
        return NombreJ;
    }

    public void setNombreJ(String NombreJ) {
        this.NombreJ = NombreJ;
    }

    public String getPrecioJ() {
        return PrecioJ;
    }

    public void setPrecioJ(String PrecioJ) {
        this.PrecioJ = PrecioJ;
    }

    public String getDescripcionJ() {
        return DescripcionJ;
    }

    public void setDescripcionJ(String DescripcionJ) {
        this.DescripcionJ = DescripcionJ;
    }
    
    
}
